package com.monami.app.reply.dao;

public class ReplyDTO {
	private int an_idx;
	private int iqr_idx;
	private String admin_id;
	private String answer;
	public int getAn_idx() {
		return an_idx;
	}
	public void setAn_idx(int an_idx) {
		this.an_idx = an_idx;
	}
	public int getIqr_idx() {
		return iqr_idx;
	}
	public void setIqr_idx(int iqr_idx) {
		this.iqr_idx = iqr_idx;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	
	
}
